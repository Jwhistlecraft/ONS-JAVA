package BankConstructorOverload;

public class Accounts {
	public Accounts(int a) {
		System.out.println("hello 1");
	}
	public Accounts(int a, int B) {
		System.out.println("hello 12");
	}
	public Accounts(int a, int B, int C) {
		System.out.println("hello 123");
	}
	public Accounts(int a, int B, int C, int D) {
		System.out.println("hello 1234");
	}	
}
